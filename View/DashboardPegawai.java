    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String idPegawai = SesiAplikasi.getIdPegawai();

        if (idPegawai == null) {
            JOptionPane.showMessageDialog(this, "Sesi Pegawai hilang. Gagal mengambil gaji.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, "yakin mau ambil gaji?", "konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            boolean sukses = pc.ambilGaji(idPegawai);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Gaji berhasil diambil. Status dan nominal gaji diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                DataPegawai();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengambil gaji. Mungkin gaji sudah diambil atau belum ada gaji bulan ini.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton5ActionPerformed

