    private void DataPegawai() {
        String idPegawai = SesiAplikasi.getIdPegawai();
        String namaPegawai = SesiAplikasi.getNama();
        //riwayatAbsen
        try {
            DefaultTableModel dtmAbsen = absenCtrl.riwayatAbsen(idPegawai);
            if (dtmAbsen != null) {
                TableTampilRiwayatAbsen.setModel(dtmAbsen);
            }
        } catch (Exception e) {
            System.out.println("gagal memuat" + e.getMessage());
        }
        //ambil gaji
        try {
            DefaultTableModel dtmGaji = pc.tampilkanGajiPribadi(idPegawai);
            if (dtmGaji != null) {
                TableambilGaji.setModel(dtmGaji);
                jTable.setModel(dtmGaji);
            }//pengen nampilin 2 data di tab berbeda
        } catch (Exception e) {
            System.out.println("gagal memuat:" + e.getMessage());
        }
        //ubah data pribadi
        try {
            // Panggil method yang mengembalikan object Pegawai
            Pegawai dataPribadi = pc.getPegawaiDataPribadi(idPegawai);

            if (dataPribadi != null) {
                // Mengisi komponen di tab UbahDataPribadi (jPanel1)
                lblUId1.setText(dataPribadi.getId_pegawai());
                txtUNoHpUbahDataPribadi.setText(dataPribadi.getNo_telp());
                txtUEmailubahdatapribadi.setText(dataPribadi.getEmail());
            }
        } catch (Exception e) {
            System.err.println("Error memuat Data Pribadi: " + e.getMessage());
        }
    }
